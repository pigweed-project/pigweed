TEST(ChaCha20, Encrypt) {
  std::array<std::byte, sizeof(kTestPlaintext)> output;
  PW_TEST_ASSERT_OK(Crypt(
      kTestKey, kTestNonce, as_bytes(span(kTestPlaintext)), span(output)));
  EXPECT_EQ(memcmp(output.data(), kTestCiphertext, output.size()), 0);
}

TEST(ChaCha20, Decrypt) {
  std::array<std::byte, sizeof(kTestCiphertext)> output;
  PW_TEST_ASSERT_OK(Crypt(
      kTestKey, kTestNonce, as_bytes(span(kTestCiphertext)), span(output)));
  EXPECT_EQ(memcmp(output.data(), kTestPlaintext, output.size()), 0);
}

TEST(ChaCha20, EncryptDecryptEmpty) {
  std::array<std::byte, 0> output;
  PW_TEST_ASSERT_OK(Crypt(kTestKey, kTestNonce, {}, span(output)));
}

TEST(ChaCha20, InPlace) {
  auto plainSpan = as_bytes(span(kTestPlaintext));
  auto cipherSpan = as_bytes(span(kTestCiphertext));
  std::vector<std::byte> buffer(plainSpan.begin(), plainSpan.end());
  PW_TEST_ASSERT_OK(Crypt(kTestKey, kTestNonce, buffer, span(buffer)));
  ASSERT_EQ(buffer.size(), cipherSpan.size());
  EXPECT_EQ(memcmp(buffer.data(), cipherSpan.data(), buffer.size()), 0);

  // Decrypt InPlace
  PW_TEST_ASSERT_OK(Crypt(kTestKey, kTestNonce, buffer, span(buffer)));
  ASSERT_EQ(buffer.size(), plainSpan.size());
  EXPECT_EQ(memcmp(buffer.data(), plainSpan.data(), buffer.size()), 0);
}

TEST(ChaCha20, InvalidArgs) {
  std::array<std::byte, sizeof(kTestPlaintext)> output;
  constexpr std::array<std::byte, kKeySizeBytes - 1> bad_key = {};
  constexpr std::array<std::byte, kNonceSizeBytes - 1> bad_nonce = {};
  std::array<std::byte, sizeof(kTestPlaintext) - 1> small_output;

  EXPECT_EQ(Crypt(bad_key, kTestNonce, as_bytes(span(kTestPlaintext)), output),
            Status::InvalidArgument());
  EXPECT_EQ(Crypt(kTestKey, bad_nonce, as_bytes(span(kTestPlaintext)), output),
            Status::InvalidArgument());
  EXPECT_EQ(
      Crypt(kTestKey, kTestNonce, as_bytes(span(kTestPlaintext)), small_output),
      Status::InvalidArgument());
}

